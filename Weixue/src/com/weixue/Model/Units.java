package com.weixue.Model;

public class Units {
private int unitID;
private int courseID;
private String unitName;
private String unitIntroduction;
private int unitSort;
public int getUnitID() {
	return unitID;
}
public void setUnitID(int unitID) {
	this.unitID = unitID;
}
public int getCourseID() {
	return courseID;
}
public void setCourseID(int courseID) {
	this.courseID = courseID;
}
public String getUnitName() {
	return unitName;
}
public void setUnitName(String unitName) {
	this.unitName = unitName;
}
public String getUnitIntroduction() {
	return unitIntroduction;
}
public void setUnitIntroduction(String unitIntroduction) {
	this.unitIntroduction = unitIntroduction;
}
public int getUnitSort() {
	return unitSort;
}
public void setUnitSort(int unitSort) {
	this.unitSort = unitSort;
}

}
